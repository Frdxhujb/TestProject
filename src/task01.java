import java.util.Scanner;

public class task01 {
    public static void main(String[] args) {
        System.out.println("Введите число");
        Scanner scanner = new Scanner(System.in);


        int a = scanner.nextInt();


        for (int i = 0; i < a; i++) {

            {
                System.out.println();
            }

            for (int j = 0; j < a; j++) {


                System.out.printf(" * ");
            }


        } System.out.println();

    }
}
